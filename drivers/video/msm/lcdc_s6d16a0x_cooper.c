static void determine_lcd_type(unsigned char* ids)
{
	unsigned char data[4] = {0, };

	DPRINT("start %s\n", __func__);	
	
	// read MTP from LDI
	read_ldi_register(RDID1, &data[0], 1);

	if( data[0] == 0x6B )
	{
	read_ldi_register(RDID2, &data[1], 1);
	read_ldi_register(RDID3, &data[2], 1);
		printk ( "@@@@@     %s : read by SDI == AUO or SONY\n", __func__ );
	}
	else
	{
		data[0] = 0;
		data[1] = 0;
		data[2] = 0;
		read_ldi_register_sdo(RDID1, &data[0], 1);
		read_ldi_register_sdo(RDID2, &data[1], 1);
		read_ldi_register_sdo(RDID3, &data[2], 1);
		printk ( "@@@@@     %s : read by SDO == SMD\n", __func__ );
	}

	printk("ldi mtpdata: %x %x %x\n", data[0], data[1], data[2]);

	if(data[0]==mtp_value_smd[0] && data[1]==mtp_value_smd[1] && data[2]==mtp_value_smd[2])
	{
		printk("lcd type is SMD\n");
		lcd_type = LcdTypeSMD;
		lcd_type_smd = 1;
	}
	else if(data[0]==mtp_value_sony[0] && data[1]==mtp_value_sony[1] && data[2]==mtp_value_sony[2])
	{
		printk("lcd type is SONY\n");
		lcd_type = LcdTypeSONY;
	}
	else if(data[0]==mtp_value_auo[0] && data[1]==mtp_value_auo[1] && data[2]==mtp_value_auo[2])
	{
		printk("lcd type is AUO\n");
		lcd_type = LcdTypeAUO;
	}
	else
	{
		printk("lcd type is AUO\n");
		lcd_type = LcdTypeAUO;
//		printk("default lcd type is SMD\n");
//		lcd_type = LcdTypeSMD;
	}
}

static void spi_init(void)
{
	/* Setting the Default GPIO's */
	spi_sclk = *(lcdc_s6d16a0x_pdata->gpio_num);
	spi_cs   = *(lcdc_s6d16a0x_pdata->gpio_num + 1);
	spi_sdi  = *(lcdc_s6d16a0x_pdata->gpio_num + 2);
	spi_sdo  = *(lcdc_s6d16a0x_pdata->gpio_num + 3);
	lcd_reset= *(lcdc_s6d16a0x_pdata->gpio_num + 4);

	/* Set the output so that we dont disturb the slave device */
	gpio_set_value(spi_sclk, 0);
	gpio_set_value(spi_sdi, 0);
	gpio_set_value(spi_sdo, 0);

	/* Set the Chip Select De-asserted */
	gpio_set_value(spi_cs, 0);

}

#define VREG_ENABLE	1
#define VREG_DISABLE	0

static void s6d16a0x_vreg_config(int vreg_en)
{
	int rc;
	struct vreg *vreg_lcd =NULL;

	if (vreg_lcd == NULL) {
		if( board_hw_revision >= 0x2 )
			vreg_lcd = vreg_get(NULL, "ldo4");
		else
			vreg_lcd = vreg_get(NULL, "ldo3");

		if (IS_ERR(vreg_lcd)) {
			printk(KERN_ERR "%s: vreg_get(%s) failed (%ld)\n",
				__func__, "ldo3 or 4", PTR_ERR(vreg_lcd));
			return;
		}

		rc = vreg_set_level(vreg_lcd, OUT3000mV);
		if (rc) {
			printk(KERN_ERR "%s: LCD set_level failed (%d)\n",
				__func__, rc);
		}
	}

	if (vreg_en) {
		rc = vreg_enable(vreg_lcd);
		if (rc) {
			printk(KERN_ERR "%s: LCD enable failed (%d)\n",
				 __func__, rc);
		}
	} else {
		rc = vreg_disable(vreg_lcd);
		if (rc) {
			printk(KERN_ERR "%s: LCD disable failed (%d)\n",
				 __func__, rc);
		}
	}
}

static void s6d16a0x_disp_powerup(void)
{
	DPRINT("start %s\n", __func__);	

	if (!s6d16a0x_state.disp_powered_up && !s6d16a0x_state.display_on) {

		s6d16a0x_vreg_config(VREG_ENABLE);

		if(!display_init)
			mdelay(1);
		else
			msleep(1);
		gpio_tlmm_config(GPIO_CFG(101, 0, GPIO_CFG_OUTPUT, GPIO_CFG_NO_PULL, GPIO_CFG_2MA), GPIO_CFG_ENABLE);
		gpio_set_value(lcd_reset, 0);
		if(!display_init)
			mdelay(20);
		else
			msleep(20);
		//LCD_RESET_N_HI
		gpio_set_value(lcd_reset, 1);
		if(!display_init)
			mdelay(50);
		else
			msleep(50);

		s6d16a0x_state.disp_powered_up = TRUE;
	}
}

static void s6d16a0x_disp_powerdown(void)
{
	DPRINT("start %s\n", __func__);	
	if( lcd_type_smd == 0 )
	{
	gpio_tlmm_config(GPIO_CFG(lcd_reset, 0, GPIO_CFG_OUTPUT, GPIO_CFG_NO_PULL, GPIO_CFG_2MA), GPIO_CFG_ENABLE);
	gpio_set_value(lcd_reset, 0);
	}

	s6d16a0x_vreg_config(VREG_DISABLE);
	msleep(1);

	s6d16a0x_state.disp_powered_up = FALSE;
}

static void s6d16a0x_disp_on(void)
{
	int i;

	DPRINT("start %s\n", __func__);	

	if (s6d16a0x_state.disp_powered_up && !s6d16a0x_state.display_on) {
		printk("Cooper HW rev is %d, apply %d's init sequence\n",board_hw_revision,board_hw_revision);

		if(lcd_type == LcdTypeSMD)
		{
			for (i = 0; i < POWER_ON_SETTINGS(power_on_setting_table_smd); i++)
				setting_table_write(&power_on_setting_table_smd[i]);			
		}
		else if(lcd_type == LcdTypeSONY)
		{
			for (i = 0; i < POWER_ON_SETTINGS(power_on_setting_table_sony); i++)
				setting_table_write(&power_on_setting_table_sony[i]);			
		}
		else//if(lcd_type == LcdTypeAUO)
		{
			if( board_hw_revision >= 0x2 )
			{
				for (i = 0; i < POWER_ON_SETTINGS(power_on_setting_table_auo_hw2); i++)
					setting_table_write(&power_on_setting_table_auo_hw2[i]);			
			}
			else
			{
				for (i = 0; i < POWER_ON_SETTINGS(power_on_setting_table_auo); i++)
					setting_table_write(&power_on_setting_table_auo[i]);			
			}
		}
		s6d16a0x_state.display_on = TRUE;
		lcd_on_state_for_debug = TRUE;
	}
}

static int lcdc_s6d16a0x_panel_on(struct platform_device *pdev)
{
	unsigned char data[4] = {0, };

	DPRINT("start %s\n", __func__);	

	if (!s6d16a0x_state.disp_initialized) {
		/* Configure reset GPIO that drives DAC */
		lcdc_s6d16a0x_pdata->panel_config_gpio(1);
		s6d16a0x_disp_powerup();
		spi_init();	/* LCD needs SPI */

		if( lcd_type == 0 )
		{
			// Sleep Out before ID read
			setting_table_write(&power_on_setting_table_sleep_out[0]);

			// read MTP from LDI
			determine_lcd_type(data);
		}

		s6d16a0x_disp_on();		
		s6d16a0x_state.disp_initialized = TRUE;
	}
	display_init = TRUE;
	return 0;
}

static int lcdc_s6d16a0x_panel_off(struct platform_device *pdev)
{
	int i;

	DPRINT("start %s\n", __func__);	

	if (s6d16a0x_state.disp_powered_up && s6d16a0x_state.display_on) {
		
		for (i = 0; i < POWER_OFF_SETTINGS; i++)
			setting_table_write(&power_off_setting_table[i]);	
		if(lcd_type == LcdTypeSMD)
		{
			msleep(100);		// 100 ms more needed in case of SMD panel ( SMD SPEC )
		}
		lcdc_s6d16a0x_pdata->panel_config_gpio(0);

		s6d16a0x_state.display_on = FALSE;
		lcd_on_state_for_debug = FALSE;
		s6d16a0x_state.disp_initialized = FALSE;
		s6d16a0x_disp_powerdown();
	}
	return 0;
}

